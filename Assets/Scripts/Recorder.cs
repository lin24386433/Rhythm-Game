    private void Start()
    {

        //Load the AudioSource attached to the Conductor GameObject
        musicSource = GetComponent<AudioSource>();

        musicLength = musicSource.clip.length;

        totalBeats = (Mathf.RoundToInt((songBpm * musicLength) / 60));

        //notes = new int[totalBeats, 4];


        //Calculate the number of seconds in each beat
        secPerBeat = 60f / songBpm;

        beatPerSec = 1 / secPerBeat;

        //Record the time when the music starts
        dspSongTime = (float)AudioSettings.dspTime;

        musicSlider.minValue = 0;
        musicSlider.maxValue = totalBeats;

        bpmNowText.text = (musicSlider.value).ToString();

        //StartCoroutine(waittime(secPerBeat));
    }
    void Update()
    {
        if (OnPlay)
        {
            time = musicSource.time;

            //determine how many seconds since the song started
            songPosition = (float)(AudioSettings.dspTime - dspSongTime);

            //determine how many beats since the song started
            songPositionInBeats = time / secPerBeat;

            beatNow = (int)songPositionInBeats;

            musicSlider.value = beatNow;

            bpmNowText.text = (musicSlider.value).ToString();
        }
        if(beatNow == totalBeats)
        {
            OnPlay = false;
        }
        record(beatNow);
        ShowNotesInBeats(beatNow);
        
    }

    void record(int bpm)
    {
        if (bpm == totalBeats) return;
        if (Input.GetKeyDown(KeyCode.Z))
        { 
            if(notes[bpm, 0] == 0)
            {
                notes[bpm, 0] = 1;
            }
            else if (notes[bpm, 0] == 1)
            {
                notes[bpm, 0] = 2;
            }
            else if (notes[bpm, 0] == 2)
            {
                notes[bpm, 0] = 0;
            }
        }
        if (Input.GetKeyDown(KeyCode.X))
        {
            if (notes[bpm, 1] == 0)
            {
                notes[bpm, 1] = 1;
            }
            else if (notes[bpm, 1] == 1)
            {
                notes[bpm, 1] = 2;
            }
            else if (notes[bpm, 1] == 2)
            {
                notes[bpm, 1] = 0;
            }
        }
        if (Input.GetKeyDown(KeyCode.C))
        {
            if (notes[bpm, 2] == 0)
            {
                notes[bpm, 2] = 1;
            }
            else if (notes[bpm, 2] == 1)
            {
                notes[bpm, 2] = 2;
            }
            else if (notes[bpm, 2] == 2)
            {
                notes[bpm, 2] = 0;
            }
        }
        if (Input.GetKeyDown(KeyCode.V))
        {
            if (notes[bpm, 3] == 0)
            {
                notes[bpm, 3] = 1;
            }
            else if (notes[bpm, 3] == 1)
            {
                notes[bpm, 3] = 2;
            }
            else if (notes[bpm, 3] == 2)
            {
                notes[bpm, 3] = 0;
            }
        }
    }

    void ShowNotesInBeats(int bpm)
    {
        if (bpm == totalBeats) return;
        for (int i = 0; i <= 3; i++)
        {
            if(notes[bpm,i] == 1)
            {
                notesObj[i].SetActive(true);
                notesObj[i].GetComponent<SpriteRenderer>().color = Color.white;
            }
            else if (notes[bpm, i] == 2)
            {
                notesObj[i].SetActive(true);
                notesObj[i].GetComponent<SpriteRenderer>().color = Color.yellow;
            }
            else
            {
                notesObj[i].SetActive(false);
            }
        }
    }

    public void OnPlayBtnPressed()
    {
        songPositionInBeats = musicSlider.value;
        musicSource.time = songPositionInBeats * secPerBeat;
        //Start the music
        musicSource.Play();
        OnPlay = true;
    }

    public void OnStopBtnPressed()
    {
        //Stop the music
        musicSource.Pause();
        OnPlay = false;
    }

    public void OnSaveBtnPressed()
    {
        string saveStr = "";
        for(int i = 0; i< totalBeats; i++)
        {
            saveStr += "{";
            for (int j = 0; j <= 3; j++)
            {
                if(j==3)
                    saveStr += notes[i,j].ToString();
                else
                    saveStr += notes[i, j].ToString() + ",";
            }
            if(i == totalBeats-1)
                saveStr += "} \n";
            else
                saveStr += "}, \n";
        }
        Debug.Log(saveStr);
    }

    public void OnSliderValueChanged()
    {
        beatNow = (int)(musicSlider.value);
        bpmNowText.text = (musicSlider.value).ToString();
        ShowNotesInBeats(Mathf.RoundToInt(songPositionInBeats));
    }

